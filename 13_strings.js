// Using single quotes
let a = 'Hello, World!';
console.log(a);

// Using double quotes
let b = "JavaScript is fun!";
console.log(b);


// Concatenation
let firstName = 'Binayak';
let lastName = 'lamsal';
let fullName = firstName + ' ' + lastName;
console.log(fullName);

// String interpolation (Template literals)
let age = 30;
let m = `My name is ${fullName} and I am ${age} years old.`;
console.log(m);
