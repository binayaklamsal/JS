//Operators in Js


/* Arithmetic Operators */

let a = 4;
let b = 5;
console.log("a + b =", a+b)
console.log("a - b =", a-b)
console.log("a / b =", a/b)
console.log("a * b =", a*b)
console.log("a ** b =", a**b) // ** is to the power
console.log("a % b =", a%b)
console.log("a++ =", a++)
console.log("++a =", ++a)
console.log("a++ =", a++)

/* Assignment Operators */

let m = 5
m += 7 // same as m = m + 5, same goes to subs -= , multi *=
console.log(m)

/* Comparison Operator */
 
let c1 = 5;
let c2 = 6;
console.log("c1 == c2", c1 == c1)
console.log("c1 == c2", c1 != c1)

/* Logical Operator */

let x = 5;
let y = 6;
console.log(x<y && x==5) // AND : only T and T will be true else all are false
console.log(x>y || x==5) // OR : if one of them is T then true else all are fasle
console.log(!false)// Not flase
console.log(!true)// Not True
